	public function addWishlist($product_id, $user_id) {
		$cnt = $this->checkWishlist($user_id, $product_id);
		if($cnt > 0) {
			$this->db->query("DELETE FROM " . DB_PREFIX . "customer_wishlist WHERE customer_id = '" . (int)$user_id . "' AND product_id = '" . (int)$product_id . "'");
		}
